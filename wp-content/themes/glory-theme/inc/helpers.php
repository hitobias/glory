/**
 * Truncate text to a specified length
 */
function glory_truncate(string $text, int $length = 100, string $suffix = '...'): string {
    $text = wp_strip_all_tags($text);
    if (mb_strlen($text) <= $length) {
        return $text;
    }
    return mb_substr($text, 0, $length) . $suffix;
}

/**
 * Get reading time estimate
 */
function glory_reading_time(int $post_id = 0): string {
    if (!$post_id) {
        $post_id = get_the_ID();
    }

    $content = get_post_field('post_content', $post_id);
    $word_count = mb_strlen(wp_strip_all_tags($content));
    // Chinese: ~500 characters per minute
    $minutes = max(1, (int) ceil($word_count / 500));

    return sprintf(__('約 %d 分鐘', 'glory-theme'), $minutes);
}

/**
 * Get formatted post date
 */
function glory_post_date(int $post_id = 0): string {
    if (!$post_id) {
        $post_id = get_the_ID();
    }
    return get_the_date('Y年n月j日', $post_id);
}

/**
 * Check if current user can access volunteer resources
 */
function glory_can_access_volunteer_area(): bool {
    if (!is_user_logged_in()) {
        return false;
    }

    $user = wp_get_current_user();
    return in_array('volunteer', $user->roles, true)
        || in_array('administrator', $user->roles, true)
        || in_array('editor', $user->roles, true);
}

